fun main() {
//      Characters are represented by the type Char. Character literals go in single quotes: '1'.
//      Special characters start from an escaping backslash "\"

//      SPECIAL CHARACTER :
//      \t – tab

//      \b – backspace

//      \n – new line (LF)

//      \r – carriage return (CR)

//      \' – single quotation mark

//      \" – double quotation mark

//      \\ – backslash

//      \$ – dollar sign

    val dChar: Char = 'D' // Need a single quotation mark instead of double
    val aChar: Char = 'A'
    val fChar: Char = 'F'


    print(dChar);print('\n')
    print(aChar);print('\n')
    print(fChar);print('\n')
    print(fChar);print('\n')
    print(aChar)


}